
numbers_list = [1, 2, 3, 4, 5, 4, 5]
print(numbers_list)

# pop(), Remove and return item at index (default last).
numbers_list.pop()
numbers_list.pop(1)

# remove(), Remove first occurrence of value.
numbers_list.remove(4)
numbers_list.remove(4)

# clear()
numbers_list.clear()

print(numbers_list)
